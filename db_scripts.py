def get_question_after(question_id, quiz_id):
    """
    Возвращает следующий вопрос для указанной викторины.
    Параметры:
        question_id – id последнего показанного вопроса из таблицы quiz_content (0 для первого)
        quiz_id – id викторины
    Возвращает кортеж:
        (id_записи_в_quiz_content, текст_вопроса, правильный_ответ, wrong1, wrong2, wrong3)
    или None, если вопросов больше нет.
    """
    open_database()
    query = ''' SELECT quiz_content.id, question.question, question.answer, question.wrong1, question.wrong2, question.wrong3
    FROM question, quiz_content
    WHERE quiz_content.question_id == question.id
    AND quiz_content.id > ? AND quiz_content.quiz_id == ?
    ORDER BY quiz_content.id '''
    cursor.execute(query, [question_id, quiz_id])
    result = cursor.fetchone()
    close_database()
    return result


def check_answer(question_id, user_answer):
    """
    Проверяет, совпадает ли ответ пользователя с правильным ответом на вопрос.
    Возвращает True, если ответ верный, иначе False.
    """
    open_database()
    cursor.execute('SELECT answer FROM question WHERE id = ?', (question_id,))
    result = cursor.fetchone()
    close_database()
    if result:
        return result[0] == user_answer
    return False


