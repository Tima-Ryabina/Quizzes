import sqlite3

# === Глобальные настройки подключения к БД ===
db_name = 'quiz.sqlite'      # имя файла базы данных
connection = None            # объект соединения с БД (будет создан позже)
cursor = None                # объект курсора для выполнения запросов


def open_database():
    """Открывает соединение с БД и создаёт курсор."""
    global connection, cursor
    connection = sqlite3.connect(db_name)
    cursor = connection.cursor()


def close_database():
    """Закрывает курсор и соединение с БД."""
    cursor.close()
    connection.close()


def do(query):
    """Выполняет SQL-запрос (обычно INSERT, UPDATE, DELETE) и фиксирует изменения."""
    cursor.execute(query)
    connection.commit()


def clear_database():
    """Удаляет все таблицы (сбрасывает схему БД)."""
    open_database()
    # Порядок удаления важен из-за внешних ключей: сначала дочерние, потом родительские
    query = '''DROP TABLE IF EXISTS quiz_content'''
    do(query)
    query = '''DROP TABLE IF EXISTS question'''
    do(query)
    query = '''DROP TABLE IF EXISTS quiz'''
    do(query)
    close_database()


def create_tables():
    """Создаёт три таблицы: quiz (викторины), question (вопросы), quiz_content (связи)."""
    open_database()
    # Включаем поддержку внешних ключей для этого соединения
    cursor.execute('''PRAGMA foreign_keys = ON''')

    # Таблица викторин
    do('''CREATE TABLE IF NOT EXISTS quiz (
        id INTEGER PRIMARY KEY,
        name VARCHAR)''')
    
    # Таблица вопросов (текст вопроса, правильный ответ, три неверных варианта)
    do('''CREATE TABLE IF NOT EXISTS question (
        id INTEGER PRIMARY KEY,
        question VARCHAR,
        answer VARCHAR,
        wrong1 VARCHAR,
        wrong2 VARCHAR,
        wrong3 VARCHAR)''')
    
    # Таблица связей: к какой викторине (quiz_id) относится какой вопрос (question_id)
    do('''CREATE TABLE IF NOT EXISTS quiz_content (
        id INTEGER PRIMARY KEY,
        quiz_id INTEGER,
        question_id INTEGER,
        FOREIGN KEY (quiz_id) REFERENCES quiz (id),
        FOREIGN KEY (question_id) REFERENCES question (id) )''')
    close_database()


